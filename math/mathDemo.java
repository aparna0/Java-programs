import java.lang.Math;
public class mathDemo {
public static void main(String args[]) {
    System.out.println("minimum between 10 & 5.5  : "+Math.min(10, 5.5));
    System.out.println("maximum between 10.5 & 5.5  : "+Math.max(10.5, 5.5));
    System.out.println(Math.pow(2,3));
    System.out.println(Math.sqrt(25.5));
    System.out.println(" e^ 25 : "+Math.exp(25));
    System.out.println(Math.ceil(25.6));
    System.out.println(Math.floor(25.6));
    System.out.println("next value nearest to : 25 "+Math.nextUp(25));
    System.out.println("next value nearest to : 25.5"+ Math.nextUp(25.5));
    System.out.println(Math.round(25.5));
    System.out.println(Math.sin(90));
    System.out.println(Math.cos(90));
    System.out.println(Math.tan(90));
    System.out.println(Math.log(90));
}
}